// Authoring-time codegen: simplified world landmass as one SVG path, projected
// with the exact equirectangular transform MapView uses. Source data is
// vendored in tools/data/countries.geo.json (world.geo.json, public domain).
// Run `npm run gen:worldmap` after changing projection constants or tolerance.
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const geo = JSON.parse(readFileSync(join(root, 'tools/data/countries.geo.json'), 'utf8'))

// Must match src/ui/MapView.tsx exactly.
const W = 960
const H = 420
const px = (lon) => ((lon + 180) / 360) * W
const py = (lat) => ((90 - lat) / 180) * ((H * 175) / 180)

// Douglas–Peucker simplification in projected pixel space.
function simplify(points, tolerance) {
  if (points.length < 3) return points
  const keep = new Array(points.length).fill(false)
  keep[0] = keep[points.length - 1] = true
  const stack = [[0, points.length - 1]]
  while (stack.length) {
    const [a, b] = stack.pop()
    const [ax, ay] = points[a]
    const [bx, by] = points[b]
    const dx = bx - ax
    const dy = by - ay
    const norm = Math.hypot(dx, dy)
    let maxDist = -1
    let maxIdx = -1
    for (let i = a + 1; i < b; i++) {
      const [x, y] = points[i]
      // Closed rings start and end on the same point — fall back to distance
      // from the endpoint, or DP sees zero deviation everywhere.
      const dist =
        norm < 1e-6
          ? Math.hypot(x - ax, y - ay)
          : Math.abs(dy * x - dx * y + bx * ay - by * ax) / norm
      if (dist > maxDist) {
        maxDist = dist
        maxIdx = i
      }
    }
    if (maxDist > tolerance) {
      keep[maxIdx] = true
      stack.push([a, maxIdx], [maxIdx, b])
    }
  }
  return points.filter((_, i) => keep[i])
}

const TOLERANCE = 0.9 // px
const MIN_RING_POINTS = 6

const rings = []
for (const feature of geo.features) {
  if (feature.id === 'ATA') continue // Antarctica is clipped off the map
  const geom = feature.geometry
  const polys = geom.type === 'Polygon' ? [geom.coordinates] : geom.coordinates
  for (const poly of polys) {
    const outer = poly[0] // outer ring only; lakes are invisible at this scale
    const projected = outer.map(([lon, lat]) => [px(lon), py(lat)])
    const simplified = simplify(projected, TOLERANCE)
    if (simplified.length >= MIN_RING_POINTS) rings.push(simplified)
  }
}

const d = rings
  .map((ring) => {
    const coords = ring.map(([x, y]) => `${x.toFixed(1)},${y.toFixed(1)}`)
    return `M${coords.join('L')}Z`
  })
  .join('')

// Second artifact: the same landmass as simplified lon/lat rings, for the
// globe (orthographic) projection which must re-project every frame.
// Antarctica is included here — a globe has a south pole.
const SPHERE_TOLERANCE_DEG = 0.4
const sphereRings = []
for (const feature of geo.features) {
  const geom = feature.geometry
  const polys = geom.type === 'Polygon' ? [geom.coordinates] : geom.coordinates
  for (const poly of polys) {
    const simplified = simplify(poly[0], SPHERE_TOLERANCE_DEG)
    if (simplified.length >= MIN_RING_POINTS) {
      sphereRings.push(simplified.map(([lon, lat]) => [Math.round(lon * 10) / 10, Math.round(lat * 10) / 10]))
    }
  }
}
const ringsJson = JSON.stringify(sphereRings)

const out = `// GENERATED by tools/gen-worldmap.mjs — do not edit by hand.
// Simplified world landmass, projected to the ${W}×${H} map viewport.
export const WORLD_PATH = '${d}'

// The same landmass as [lon, lat] rings (Antarctica included) for the globe
// projection, which re-projects at render time.
export const WORLD_RINGS: readonly (readonly (readonly [number, number])[])[] = ${ringsJson}
`
writeFileSync(join(root, 'src/data/worldmap.gen.ts'), out)
console.log(
  `wrote ${rings.length} flat rings (${Math.round(d.length / 1024)}KB) + ${sphereRings.length} sphere rings (${Math.round(ringsJson.length / 1024)}KB)`,
)
