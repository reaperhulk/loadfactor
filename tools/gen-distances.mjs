// Authoring-time codegen: great-circle distances between every city pair.
// The engine bans non-spec-pinned float math (sin/cos/asin), so distances are
// computed here — with whatever math we like — and shipped as integer data.
// Run `npm run gen:distances` after editing src/data/cities.json.
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const cities = JSON.parse(readFileSync(join(root, 'src/data/cities.json'), 'utf8'))

const EARTH_RADIUS_KM = 6371
const rad = (deg) => (deg * Math.PI) / 180

function haversineKm(a, b) {
  const dLat = rad(b.lat - a.lat)
  const dLon = rad(b.lon - a.lon)
  const h =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(rad(a.lat)) * Math.cos(rad(b.lat)) * Math.sin(dLon / 2) ** 2
  return Math.round(2 * EARTH_RADIUS_KM * Math.asin(Math.sqrt(h)))
}

const ids = cities.map((c) => c.id).sort()
const lines = []
for (let i = 0; i < ids.length; i++) {
  for (let j = i + 1; j < ids.length; j++) {
    const a = cities.find((c) => c.id === ids[i])
    const b = cities.find((c) => c.id === ids[j])
    lines.push(`  '${a.id}-${b.id}': ${haversineKm(a, b)},`)
  }
}

const out = `// GENERATED by tools/gen-distances.mjs — do not edit by hand.
// Great-circle distances in km, keyed '<idA>-<idB>' with idA < idB.
export const DISTANCES_KM: Record<string, number> = {
${lines.join('\n')}
}
`
writeFileSync(join(root, 'src/data/distances.gen.ts'), out)
console.log(`wrote ${lines.length} pairs for ${ids.length} cities`)
